    <h2>Chocolate Chip Cookie Recipe</h2>
    <br>
    <hr><br>
    <h3>Ingredients</h3>
    <h4>For a Batch of 12 cookies</h4>
    <ul>
        <li>•½ cup granulated sugar (100 g)</li>
        <li>•¾ cup brown sugar(165 g)</li>
        <li>•1 teaspoon salt</li>
        <li>•½ cup melted unsalted butter (115 g)</li>
        <li>•1 egg</li>
        <li>•1 teaspoon vanilla extract</li>
        <li>•1 ¼ cups all-purpose flour(155 g)</li>
        <li>•½ teaspoon baking soda</li>
        <li>•4 oz milk OR semi-sweet chocolate chunks (110 g)</li>
        <li>•4 oz dark chocolate chunk (110 g)</li><br>


    </ul>
    <hr><br>
    <h2>Steps</h2>

    <ul>
        <li>1. In a large bowl, whisk together the sugars, salt and butter until a paste forms with no lumps.</li><br>
        <li>2. Whisk in the egg and vanilla, beating until light ribbons fall off the whisk and</li>
        <li>remain for a short while before falling back into the mixture.</li><br>
        <li>3. Sift in the flour and baking soda, then fold the mixture with a spatula</li>
        <li>(Don't overmiz since it can cause the gluten in the flour to toughen resulting in cakier cookies)</li><br>
        <li>4. Fold in the chocolate chunks, then chill the dough for at least 30 minutes. For a more intense toffee-like flavor and deeper color,</li>
        <li>chill the dough overnight. The longer the dough rests, the more complex its flavor will be.</li><br>
        <li>5. Preheat oven to 350°F (180°C). Line a baking sheet with parchment paper.</li><br>
        <li>6. Scoop the dough with an ice-cream scoop onto a parchment paper-lined baking sheet, </li>
        <li>leaving at least 4 inches (10 cm) of space between cookies and 2 inches (5 cm) of space</li>
        <li>from the edges of the pan so that the cookies can spread evenly.</li><br>
        <li>7. Bake for 12-15 minutes, or until the edges have started to barely brown.</li><br>
        <li>8. Cool completely before serving.</li>

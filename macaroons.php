    <h1>MACARONS</h1><br>

    <img src="macaroon.png" width="350" height="auto">
    <h2>Macarons Recipe</h2>
    <br>
    <hr><br>
    <h3>Ingredients</h3>
    <h4>For a Batch of 30 macarons</h4>
    <ul>
        <li>•1 ¾ cups powdered sugar (210 g)</li>
        <li>•1 cup almond flour (95 g)</li>
        <li>•1 teaspoon salt</li>
        <li>•3 egg whites (room temparature)</li>
        <li>•¼ cup granulated sugar (50 g)</li>
        <li>•½ teaspoon vanilla extract</li>
        <li>•2 drops pink gel food coloring</li>

    <h4>Vanilla Buttercream</h4>
        <li>•1 cup unsalted butter (230 g)</li>
        <li>•3 cups powdered sugar(360 g)</li>
        <li>•1 teaspoon vanilla extract</li>
        <li>•3 tablespoons heavy cream</li><br>

    </ul>
    <hr><br>
    <h2>Steps</h2>

    <ul>
        <li>1. Make the macarons: In the bowl of a food processor, combine the powdered sugar, almond flour, and ½ teaspoon of salt,</li>
        <li>and process on low speed, until extra fine. Sift the almond flour mixture through a fine-mesh sieve into a large bowl.</li><br>
        <li>2. In a separate large bowl, beat the egg whites and the remaining ½ teaspoon of salt with an electric hand mixer until soft peaks form. </li>
        <li>Gradually add the granulated sugar until fully incorporated and continue to beat until stiff peaks form.</li><br>
        <li>3. Add the vanilla and beat until incorporated. Add the food coloring and beat until just combined.li><br>
        <li>4. Add about ⅓ of the sifted almond flour mixture at a time to the beaten egg whites and use a spatula to gently fold until combined.</li>
        <li>After the last addition of almond flour, continue to fold slowly until the batter falls into ribbons and you can make a figure 8 while holding the spatula up.</li><br>
        <li>5. Transfer the macaron batter into a piping bag fitted with a round tip.</li><br>
        <li>6. Place 4 dots of the batter in each corner of a rimmed baking sheet,</li>
        <li>and place a piece of parchment paper over it, using the batter to help adhere the parchment to the baking sheet</li><br>
        <li>7. Pipe the macarons onto the parchment paper in 1½-inch (3-cm) circles, spacing at least 1-inch (2-cm) apart.</li><br>
        <li>8. Tap the baking sheet on a flat surface 5 times to release any air bubbles.</li><br>
        <li>9. Let the macarons sit at room temperature for 30 minutes to 1 hour, until dry to the touch.</li><br>
        <li>10. Preheat the oven to 300˚F (150˚C).</li><br>
        <li>11. Bake the macarons for 17 minutes, until the feet are well-risen and the macarons don’t stick to the parchment paper..</li><br>
        <li>12. Transfer the macarons to a wire rack to cool completely before filling.</li><br>
        <li>13. Make the buttercream: In a large bowl, add the butter and beat with a mixer for 1 minute until light and fluffy. Sift in the powdered sugar and beat until fully incorporated.</li>
        <li>Add the vanilla and beat to combine. Add the cream, 1 tablespoon at a time, and beat to combine, until desired consistency is reached.</li><br>
        <li>14. Transfer the buttercream to a piping bag fitted with a round tip.</li><br>
        <li>15. Add a dollop of buttercream to one macaron shell. Top it with another macaron shell to create a sandwich. Repeat with remaining macaron shells and buttercream.</li><br>
        <li>16. Place in an airtight container for 24 hours to “bloom”.</li><br>


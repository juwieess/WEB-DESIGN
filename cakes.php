    <img src="chocofudge.png" width="350" height="auto">
    <h2>Chocolate Fudge Cake Recipe</h2>
    <br>
    <hr><br>
    <h3>Ingredients</h3>
    <ul>
        <li>•150 ml sunflower oil</li>
        <li>•175g self-raising flour</li>
        <li>•2 tbsp cocoa powder</li>
        <li>•1 tsp bicarbonate of soda</li>
        <li>•150g caster sugar</li>
        <li>•2 tbsp golden syrup</li>
        <li>•2 large eggs (lightly beaten)</li>
        <li>•150ml semi-skimmed milk</li><br>

    <h4>For The Icing</h4>
        <li>•100g unsalted butter</li>
        <li>•225g icing sugar</li>
        <li>•40g cocoa powder</li>
        <li>•2½ tbsp milk</li><br>
    </ul>
    <hr><br>
    <h2>Steps</h2>

    <ul>
        <li>1. Heat the oven to 180°C. Oil and line the base of two 18cm sandwich tins.</li>
        <li>Sieve the flour, cocoa powder and bicarbonate of soda into a bowl. Add the caster sugar and mix well.</li><br>
        <li>2. Make a well in the centre and add the golden syrup, eggs, sunflower oil and milk.</li>
        <li>Beat well with an electric whisk until smooth.</li><br>
        <li>3. Pour the mixture into the two tins and bake for 25-30 mins until risen and firm to the touch.</li>
        <li>Remove from oven, leave to cool for 10 mins before turning out onto a cooling rack.</li><br>
        <li>4. To make the icing, beat the unsalted butter in a bowl until soft.</li>
        <li>Gradually sieve and beat in the icing sugar and cocoa powder,</li>
        <li>then add enough of the milk to make the icing fluffy and spreadable.</li><br>
        <li>5. Sandwich the two cakes together with the butter icing and cover the</li>
        <li>sides and the top of the cake with more icing.</li>
